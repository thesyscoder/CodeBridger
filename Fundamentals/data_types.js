var num = 12;
var str = "Hello, World!";
var bool = true;
var arr = [1, 3, 5];
var obj = { x: 10, y: 20 };

console.log("Number Type: ", typeof num); // Number
console.log("String Type: ", typeof str); // String
console.log("Boolean Type: ", typeof bool); // Boolean
console.log("Array Type: ", Array.isArray(arr)); // Array
console.log("Object Type: ", typeof obj); // Object
